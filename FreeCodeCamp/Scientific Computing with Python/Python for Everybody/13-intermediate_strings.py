'''string comparison'''

word = 'Banana'

if word == 'banana' :
    print('All right, bananas.')

# or:

if word < 'banana' :
    print('Your word, ' + word + ', comes before banana.')
elif word > 'banana' :
    print('Your word, ' + word + ', comes after banana.')
else :
    print('All right, bananas.')
# upper and lower case makes difference
# in general, upper case comes before any lower case

