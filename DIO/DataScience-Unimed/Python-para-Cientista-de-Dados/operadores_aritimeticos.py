produto_1 = 20
produto_2 = 10

#soma
print(produto_1 + produto_2)

#subtração
print(produto_1 - produto_2)

#divisão
print(produto_1 / produto_2)

#divisão inteira
print(produto_1 // produto_2)

#multiplicação
print(produto_1 * produto_2)

#módulo (resto da divisão)
print(produto_1 % produto_2)

#exponenciação
print(produto_1 ** produto_2)